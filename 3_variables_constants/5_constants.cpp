//]         1. Have NAMES like variables
//]         2. Value CANNOT change once declared!


        ////////////////////////////////////////////////////////////////////////////////


///     TYPES OF CONSTANTS

//]         1. LITERAL CONSTANTS: 
//>                     int     x  {12};         //int
//>                     string  y  {"Frank"};    //string
//>                     char    z  {'J'};        //char
//>                     float   f  {12.56};      //float

//]         2. DECLARED CONSTANTS: Using `const` keyword
//>                     const VariableType VariableName {};

//]         3. DEFINED CONSTANTS: Don't use this method as preprocessor does not know
//]                               TYPE check, makes it difficult to find errors
//>                     #define pi 3.14159;
