//      ________________________________________________________________________________
///     STREAMS
//]         1. Interface used to perform INPUT/OUTPUT operations 
//]         2. These are INTEGRAL part of C++ standard library
//]         3. Several kinds of streams:
//>                     a. istream (reading input)
//>                     b. ostream
//>                     c. iostream

        ////////////////////////////////////////////////////////////////////////////////

///     cout
//]         1. Standard output stream

        ////////////////////////////////////////////////////////////////////////////////

///     <<
//]         1. Insertion operator
//]         2. Used in output streams
//>                     cout <<

        ////////////////////////////////////////////////////////////////////////////////

///     FEATURES OF cout and <<
//]         1. Can be CHAINED
//>                     cout << "hi" << "how" << "are" << "you?"
//]         2. Does not at LINE BREAK automatically, use below
//>                     << endl;
//>                     << "\n";

        ////////////////////////////////////////////////////////////////////////////////

///     cin
//]         1. Standard input stream

        ////////////////////////////////////////////////////////////////////////////////

///     >>
//]         1. Extraction operator
//>                     cin >>

        ////////////////////////////////////////////////////////////////////////////////

///     FEATURES OF cin and >>
//]         1. Value is STORED in variable mentioned on RIGHT of >>
//>                     cin >> variable;
//]         2. Can be CHAINED
//>                     cin >> var1 >> var2 >> var3;
//      ________________________________________________________________________________
